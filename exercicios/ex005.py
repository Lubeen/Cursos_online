#  Antecessor e Sucessor
x1 = int(input('digite um numero'))
print(f'o numero digitado é {x1} e o antecessor dele é {x1 - 1} e o sucessor dele é {x1 + 1}')
x2 = int(input('Digite outro numero'))
print('o numero digitado é {} o antecessor dele é {} e o sucessor dele é {}'.format(x2, x2 + 1 , x2 - 1))